<!-- STORE(맛집) 데이터 베이스 -->
SELECT * FROM STORE;            <!-- 테이블 안의 데이터 모두 출력 -->

DROP TABLE STORE;               <!-- 테이블 삭제 -->

DELETE FROM STORE WHERE id = 2; <!-- 테이블안의 2번째 행 삭제 -->

ALTER TABLE STORE
    ADD (tag varchar(30));          <!-- 테이블에 새로운 열 추가-->

CREATE TABLE Community              <!-- 새로운 테이블 생성 -->
(
    id NUMBER(5) GENERATED BY DEFAULT AS IDENTITY,
    tag VARCHAR2(30),
    kind VARCHAR2(10),
    location VARCHAR2(30),
    content CLOB,
    price VARCHAR2(10),
    createdate timestamp,
    PRIMARY KEY (id)
);






